/// App-wide spacing constants.
///
/// Use these instead of hardcoded numeric values to keep layout consistent
/// and easy to adjust globally.
abstract final class AppSpacing {
  /// 4px — tight gaps, icon padding
  static const double xs = 4;

  /// 8px — small gaps between related elements
  static const double sm = 8;

  /// 16px — standard padding / margin
  static const double md = 16;

  /// 24px — larger section spacing
  static const double lg = 24;

  /// 32px — hero spacing between major sections
  static const double xl = 32;

  /// 48px — full-screen paddings, large gaps
  static const double xxl = 48;
}
